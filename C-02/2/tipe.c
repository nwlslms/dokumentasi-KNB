#include <stdio.h>

int main () {
    short umur = 20;
    int ukt = 6000000;
    long harga = 200000;
    long long faktorial = 45266345623453;
    float phi = 3.14;
    double derajat = 128.34563;
    long double derajatRudal = 45.2345234526;
    char jawaban = 'y';
    char yapping[] = "Aku sedang belajar bahasa C";
    printf("%hd\n", umur);
    printf("%d\n", ukt);
    printf("%ld\n", harga);
    printf("%lld\n", faktorial);
    printf("%f\n", phi);
    printf("%lf\n", derajat);
    printf("%Lf\n", derajatRudal);
    printf("%c\n", jawaban);
    printf("%s\n", yapping);

    //Character Sequence
    return 0;
}